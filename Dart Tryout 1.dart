void main() {
  // Integer data type (int)
  int age = 25;
  print('Age: $age'); // Output: Age: 25

  // Double data type (double)
  double temperature = 36.5;
  print('Temperature: $temperature'); // Output: Temperature: 36.5

  // String data type (String)
  String name = 'Paul Muguro';
  print('Name: $name'); // Output: Name: Paul Muguro

  // List data type (List)
  List<String> fruits = ['Apple', 'Banana', 'Orange'];
  print('Fruits: $fruits'); // Output: Fruits: [Apple, Banana, Orange]

  // Map data type (Map)
  Map<String, dynamic> person = {
    'name': 'Alice Muguro',
    'age': 30,
    'isStudent': true
  };
  print('Person: $person'); // Output: Person: {name: Alice Muguro, age: 30, isStudent: true}
}
